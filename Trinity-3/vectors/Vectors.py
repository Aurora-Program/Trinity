def increment_vectors(VECTORES):
    for key, value in VECTORES.items():
        fractal_vector = FractalVector.from_list(value)
        transcender = Transcender(fractal_vector)
        new_fractal_vector, _ = transcender.extender_fractal() # Desempaquetar la tupla
        VECTORES[key] = new_fractal_vector.to_list()
    return VECTORES

